namespace Gis.Net.Osm;

/// <summary>
/// Represents a tag used in the OpenStreetMap (OSM) data model.
/// </summary>
public enum EOsmTag
{
    /// <summary>
    /// Represents a place tag in OpenStreetMap.
    /// </summary>
    Place,

    /// <summary>
    /// Represents the Natural enum member of the EOsmTag enum.
    /// </summary>
    /// <remarks>
    /// This enum member is used to indicate natural features in the OpenStreetMap data.
    /// </remarks>
    Natural,

    /// <summary>
    /// Represents the Railway member of the EOsmTag enum.
    /// </summary>
    Railway,

    /// <summary>
    /// Represents a waterway tag.
    /// </summary>
    Waterway,

    /// <summary>
    /// Represents a building tag for OpenStreetMap data.
    /// </summary>
    Building,

    /// <summary>
    /// Represents the land use tag in the OSM data.
    /// </summary>
    LandUse,

    /// <summary>
    /// Represents a historic tag in the EOsmTag enumeration.
    /// </summary>
    Historic,

    /// <summary>
    /// Represents the type of amenity.
    /// </summary>
    Amenity,

    /// <summary>
    /// Represents the leisure tag in the OpenStreetMap data.
    /// </summary>
    Leisure,

    /// <summary>
    /// Represents the tourism tag in the OpenStreetMap data model.
    /// </summary>
    Tourism,

    /// <summary>
    /// Represents the "ManMade" enum member of the EOsmTag enum.
    /// </summary>
    ManMade,

    /// <summary>
    /// Represents the denomination tag in the EOsmTag enum.
    /// </summary>
    Denomination,

    /// <summary>
    /// Represents the type of a highway.
    /// </summary>
    Highway,

    /// <summary>
    /// Represents the type of aerial way in OpenStreetMap.
    /// </summary>
    AerialWay,

    /// <summary>
    /// Represents the enum member Boundary of the EOsmTag enum.
    /// </summary>
    Boundary,

    /// <summary>
    /// Represents an aerial way, such as a cable car, chair lift, or gondola.
    /// </summary>
    AeroWay,

    /// <summary>
    /// Represents the military tag in the EOsmTag enum.
    /// </summary>
    Military,

    /// <summary>
    /// Represents the Emergency member of the EOsmTag enum.
    /// </summary>
    Emergency,

    /// <summary>
    /// Represents the tourist tag in the OpenStreetMap (OSM) data.
    /// </summary>
    Tourist,

    /// <summary>
    /// Represents a member of the EOsmTag enum that denotes a shop.
    /// </summary>
    Shop,

    /// <summary>
    /// Represents a sport-related tag in the OpenStreetMap (OSM) data model.
    /// </summary>
    Sport,

    /// <summary>
    /// Represents the Office enum member of the EOsmTag enum.
    /// </summary>
    Office,

    /// <summary>
    /// Represents a religious denomination.
    /// </summary>
    Religion
}